<h2>International System of Units</h2>

<span style="font-weight: 400;">The International System of Units (abbreviated as "SI ) is the most widely used system of measurement in the world.</span>

<span style="font-weight: 400;">SI consists of a set of base units, a set of derived units with special names, and a set of decimal-based multipliers that are used as prefixes.</span>

&nbsp;
