<h2>Introduction</h2>
Alice heard from Carmen that alcohol is cooler than water. She wants to verify that statement, therefore, she decided to carry out an experiment.</span></span></span>She prepared a glass of water and a glass of alcohol on the table and touched them by her hands to determine how much the alcohol is cooler than water. After this simple experiment, she told Carmen that alcohol indeed feels colder than the water, but that's only because alcohol evaporates faster than water. These two liquids should have the same degree of 'coldness' according to Alice, but Carmen insisted that alcohol is indeed colder than water because it 'feels' that way.Now, suppose that you were Alice, how would you resolve this problem?You probably have figured out that to reach a conclusion, we need a physical quantity called "temperature" and an apparatus to measure the temperature, which is the thermometer. Otherwise, this question can be discussed in endless subjective ways without reaching a consensus from everyone.

So what actually is a physical quantity?

<strong>A physical quantity is a quantity that can be measured.</strong>

To investigate the magnitude (magnitude means "size") of a physical quantity, we need to do measurements using instruments.

The scientist has subdivided physical quantities into two categories, eg. fundamental/base quantities and derived quantities.
