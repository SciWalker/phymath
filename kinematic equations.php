<p class="article">
According to the table above, there are about 5 variables in consideration, namely $s,v,u, t, a$. If the values of three of the four variables are known, then the value of the fourth variable can be calculated using the kinematic equations. In this manner, the kinematic equations provide a useful means of predicting information about an object's motion if other information is known. For example, if the acceleration value and the initial and final velocity values of a skidding car is known, then the displacement of the car and the time can be predicted using the kinematic equations. The equations can be utilized for any motion that can be described as being either a constant velocity motion (an acceleration of 0 $\mathrm{ms^{-2}}$) or a constant acceleration motion. However, they can never be used over any time period during which the acceleration is changing. 
\begin{equation}
v=u+at ---\mathrm{eqn\ 1}
\end{equation}
\begin{equation}
s=\frac{1}{2} (u+v)(t) ---\mathrm{eqn\ 2}
\end{equation}

\begin{equation}
s=ut+\frac{1}{2}at^2 ---\mathrm{eqn\ 3}
\end{equation}


\begin{equation}
v^2=u^2+2as ---\mathrm{eqn\ 4}
\end{equation}

In addition, sometimes we don't use $u$ for initial velocity, but we use $v_1$ instead.

To calculate the answer using kinematics equation, we will follow these procedures:
First step- Write down the quantities which you know, and the quantity that you want to find.
Second step- Choose the equation which links these equations, and substitute in the values.
Third step- Finally, we calculate the unknown quantity.</p>

<h4 style= "text-align: center;">Example 1:</h4>
<p class="article">
Accordingly, the rocket shown in the figure at the top lifts off from rest with an acceleration of $10\ \mathrm{ms^{-2}}$. Now, Calculate the displacement of the rocket when it has reached a velocity of $1000\ \mathrm{ms^{-1}}$.

Step 1: list down the variables:
\begin{equation}
\begin{split}
u&amp;=\mathrm{0\ ms^{-1}}\\
a&amp;=\mathrm{10\ ms^{-2}}\\
v&amp;=\mathrm{1000\ ms^{-1}}
\end{split}
\end{equation}
We need to determine the displacement: $s=?$

Step 2: The equation linking $u, a, v $ and $t$ is equation 1:
\begin{equation}
v^2=u^2+2as
\end{equation}

Substituting gives:
\begin{equation}
1000^2=0+2 (10) s
\end{equation}

Step 3: The subsequent calculation gives:
\begin{equation}
s=\mathrm{50000\ m}
\end{equation}
So the rocket would have traveled a distance of 50 km upwards when its velocity reaches $1000\ \mathrm{ms^{-1}}$.
</p>

<h4 style= "text-align: center;">Example 2:</h4>
<p class="article">
A car driver travelling at $\mathrm{30\ ms^{-1}}$ saw the traffic light which is 36 m in front of him flashes orange, so he pressed the brake pedal so that he was decelerating with a magnitude of $\mathrm{12.6\ ms^{-2}}$. Do you think he is able to stop before the traffic light so that he won't break the traffic law?

Step 1:

First, list down the variables that we know:

\begin{equation}
\begin{split}
u&amp;=\mathrm{30\ ms^{-1}}\\
a&amp;=\mathrm{12.6\ ms^{-2}}\\
v&amp;=\mathrm{0\ ms^{-1}}
\end{split}
\end{equation}

We need to know the displacement, $s=?$.

Step 2:

The equation in need is the equation 4:

\begin{equation}
v^2=u^2+2as
\end{equation}
rearranging gives:
\begin{equation}
\begin{split}
s&=\frac{-u^2}{2a}\\
&=\frac{-30^2}{2\times -12.6}\\&=-900/-25.2
\end{split}
\end{equation}

Step 3:
Subsequently, the calculation gives:
\begin{equation}
s=35.7\ \mathrm{m}
\end{equation}

Therefore, the car would stop right in front of the traffic light. What a close call!
&nbsp;
</p>
<p class="article">
<h2 style= "text-align: center;">Derivations of the Kinematics Equations</h2>
1. First, by the definition $a=(v-u)/t$, we get
\begin{equation}
at=v-u
\end{equation}
or
\begin{equation}
v=u+at
\end{equation}
2. total displacement/(total time taken) = average velocity
or
\begin{equation}
s/t=(u+v)/2
\end{equation}
rearranging the formula, we get
\begin{equation}
s=\frac{1}{2} (u+v)(t) ---eqn 4
\end{equation}
3.Substituting $a=(v-u)/t$ into equation 4
\begin{equation}
\begin{split}
s&amp;=\frac{1}{2}(u+at+u)/t\\
&amp;=ut+\frac{1}{2}at^2 ---eqn 5
\end{split}
\end{equation}
4.Now, we substitute $v=u+at$ into equation 4:
\begin{equation}
\begin{split}
s&amp;=\frac{1}{2} (u+v)\frac{v-u}{a}\\
\Rightarrow 2s&amp;=\frac{v^2-u^2}{a}
\end{split}
\end{equation}
Rearrange the formula, we obtain:
\begin{equation}
v^2=u^2+2as
\end{equation}

