<h1>Visual QR</h1>
<h2>An Introduction</h2>
 <p>QR Codes are an incredibly useful tool for product advertisement in a fast manner (more about QR codes <a href="https://en.wikipedia.org/wiki/QR_code">here</a>)but their appearance leaves much to be desired. </p>
 <p>
 An ordinary QR Code consists of black and white clusters of dots that are arranged in a square pattern.  The reason for the black and white is to enhance the contrast so that the QR code is more readable by machine, however this ordinary QR code is often unappealing to viewers. 
</p>
<img src="ordinaryQR.jpg" alt="ordinary QR codes" >
 <p>
    Visual QR Code is one of the solutions to this problem, we merge the QR codes with the image to produce a hybrid that combined the data and information that subsists in QR codes and the beautiful image or logo of the company.
</p>
