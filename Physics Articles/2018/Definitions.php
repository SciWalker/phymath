<h1>Definitions</h1>
<h2>Chapter 3: Dynamics</h2>
<p>&bull; Newton's first law: A body will remain at its state of motion unless acted on by a resultant force.</p>
<p>&bull; Newton's second law: The rate of change of momentum is proportional to the magnitude of the force.</p>
<p>&bull; Newton's third law: If body A exerts a force on body B then body B will exert a force on body A of equal magnitude but in the opposite direction.</p>
<p>&bull; Principle of conservation of momentum: In a closed system, when bodies interact, the total momentum in any direction remains constant.</p>
<p>&bull; Force: Rate of change of momentum</p>
<h2>Chapter 4: Work Done, Energy and Power</h2>
<p>&bull; Energy: Ability of doing work</p>
<p>&bull; Conservation of Energy: Total energy of a closed system is constant.</p>
<p>&bull; Power: work done per unit time</p>
<p>&bull; Efficiency: Useful work done/total energy input&nbsp;<strong>&times; 100%</strong></p>
<h2>Chapter 5: Equilibrium</h2>
<p>&bull; Moment of force about a point: product of the magnitude of the force and the perpendicular distance of the point to the force</p>
<p>&bull; Torque of a couple: The product of one of the forces of a couple and the perpendicular distance between them.</p>
<p>&bull; Equilibrium: Condition where the resultant force and resultant moment on the body must be zero.</p>
<h2>Chapter 6: Density and Pressure</h2>
<p>&bull; Density: mass per unit volume</p>
<p>&bull; pressure: Normal force acting per unit surface area</p>
<h2>Chapter 7 &amp; 8: Gravitation</h2>
<p>&bull; Gravitational Field Strength: Gravitational force per unit mass</p>
<p>&bull; Newton's law of universal gravitation: Any two point masses attract each other with a force that is directly&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;proportional to the product of their masses and inversely proportional to the square of their separation.</p>
<h2>Chapter 9: Light</h2>
<p>&bull; Refraction: Change of direction of light when a light passes from a medium to another medium</p>
<p>
  &bull; Refractive Index: the ratio of the velocity of light in a vacuum to its velocity in a specified medium
</p>
<h2>Chapter 10: Nuclear Physics</h2>
<p>&bull; Isotopes: Elements with the same proton number but different number of neutron</p>
<p>&bull; Proton number: number of protons in a nucleus.</p>
<p>&bull; Nucleon number: total number of protons plus neutrons in a nucleus.</p>
<p>&bull; antiparticle: particle that has the same mass but with opposite charge.</p>
<h2>Chapter 11 &amp; 12: X-ray and CT scans</h2>
<p>&bull; Contrast: difference in degree of brightness between two areas.</p>
<p>&bull; Hardness: penetrative power of the wave.</p>
<p>&bull; Attenuation: Gradual loss of wave intensity through a medium</p>
<p>&bull; Sharpness: How well the edges of structures in an x-ray image are defined</p>