<h1>Q14-9702_w17_qp_11</h1>
<p>2017 paper (oct/nov) component 11</p>
<p>
(This question is originally from Cambridge International Examination past paper)
</p>
<p>
14) The diagram shows a motorised vehicle for carrying one person.
</p>
<div>
<img src="images/question moment.PNG" alt="Question about moment" width= 70%></div>
<p>The vehicle has two wheels on one axle. The passenger stands on a platform between the wheels.
</p>
<p>
    The weight of the machine is 600 N. Its centre of mass is 200 mm in front of the axle. The wheel
radius is 400 mm.
</p>
<p>
When stationary, a passenger of weight 600 N stands with his centre of mass 200 mm behind the
axle to balance the machine.
The motor is now switched on to provide a horizontal force of 90 N at the ground to move the
vehicle forwards.
</p>
<p>How far and in which direction must the passenger move his centre of mass to maintain balance?</p>
<ol start="A">
    <li>A 60 mm backwards</li>
    <li>B 60 mm forwards</li>
    <li>C 140 mm backwards</li>
    <li>D 140 mm forwards</li>
</ol>
<p>
    Crux of the question: identify that the horizontal force 90 N (frictional force of ground acting at the wheel that actually made the vehicle moving) is acting at the ground instead of the centre of mass.
</p>
<div>
<img src="images/problem_solution_2017.PNG" alt="solution about moment" width= 70%></div>
<p>
    Applying principle of moment:
    Clockwise moment = anticlockwise moment
</p>
<p>
    600(200)=600(x) +90(400)
Where x is the distance from the c.o.m. of person to the axle.
</p>
<p>
    \begin{equation}x=84000/60
    \end{equation}
    \begin{equation}
    =140 \mathbf{mm}
    \end{equation}
</p>
<p>
 Hence, the person needs to move forward by 60 mm in order to balance the system.

Answer: B
   
</p>
<p>
    *Just in case you are curious why the gravitational force is acting on the centre of mass of vehicle, while the 90 N horizontal force is not, keep in mind that the gravitational force acting at the centre of mass of a RIGID object is an “averaging” of all gravitational forces that are acting on the masses that constitute the object as a whole. However, since there is only one 90 N force acting at the ground (frictional force). The “average” of the 90 N force is exactly the 90 N force acting at the ground. So these two forces are acting at different locations even when they are acting at the same vehicle.

</p>

